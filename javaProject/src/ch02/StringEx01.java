package ch02;

public class StringEx01 {

	public static void main(String[] args) {
		//String은 문자열을 다루는 데이터 타입(참고로 단문자는 char라는 데이터타입을 쓴다.)
		String name = "홍길동"; //문자열은 큰 따옴표("")
		char a = 'b'; //char는 단문자를 다룸, char은 작은 따옴표 ('')
		System.out.println("내 이름은 " + name + "이다" + a);
		

	}

}
