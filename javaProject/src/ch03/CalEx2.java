package ch03;

public class CalEx2 {

	public static void main(String[] args) {
		int a = 20;
		int b = 8;
		System.out.printf("a + b =% d\n", a + b);//\n줄바꿈
		System.out.printf("a - b = %d\n", a - b);
		System.out.printf("a * b = %d\n", a * b);
		System.out.printf("a / b = %d\n", a / b);
		System.out.printf("a %% b = %d\n", a % b); //%를 2개써야 에러안남

	}

}
